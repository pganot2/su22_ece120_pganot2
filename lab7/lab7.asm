; The program below prints ASCII letters printed through characters. This is done by using a given FONT_FILE to 
; that contains the important memory addresses for each character. The program incrementally gets the address of a row starting from the starting row and inspects each column and checks whether
; the value of the address at that bit is 0 or 1. If it's 0, then we take and print the character font_data stored in address memory location x5000. If it's a 1, then we instead take and print the character
; stored in the memory location x5001. While x5002 contains the ASCII letter that would be formed after printing every single bit.


; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

.ORIG 		x3000
		
			AND R0, R0, #0			; Initalizing R0 to 0 ; Contains the value of the character to be printed at a specific bit
			AND R1, R1, #0			; Initalizing R1 to 0 ; Contains the Address of the ASCII value to be rendered in large font.
			AND R2, R2, #0			; Initalizing R2 to 0 ; Unused Register
			AND R3, R3, #0			; Initalizing R3 to 0 ; Contains the starting address of the font_data
			AND R4, R4, #0			; Initalizing R4 to 0 ; Register used as an outer loop variable to count the 16 rows of the ASCII character.
			AND R5, R5, #0			; Initalizing R5 to 0 ; Register used as an inner loop variable to count the 8 columns of each row of the ASCII character.
			AND R6, R6, #0			; Initalizing R6 to 0 ; Contains the content of the ROW address.

			LDI	R1,	LETTR			; Loading the ASCII value of the character to be rendered in large font. R_ASC <- M[R_ff + 2]		

									; Multiply the ASCII value by 16
			ADD	R1, R1, R1			; R1 = R1 + R1 (R1 x 2)
			ADD	R1, R1, R1			; (R1 x 4)
			ADD	R1, R1, R1			; (R1 x 8)
			ADD	R1, R1, R1			; (R1 x 16): Rx16 <- R_ASC x 16

			LEA R3, FONT_DATA		; R3 = R_FD <- FONT_DATA_ADDRESS. Load the Address of FONT_DATA INTO R3.


			ADD	R1, R1, R3			; R1 = R_SR <- Rx16 + R3 ; Load Starting Row Address into R1.


			ADD	R4, R4, #15			; Set R_row_count to count 16 rows of the ASCII character.
			ADD	R4, R4, #1			; In two ADD statements. As two's complement can only have up to #15.
OUTERLOOP	
			AND	R5, R5, #0			; Reset value of R_col_count to #0
			ADD R5, R5, #8			; Set R_col_count to count 8 cols of each row.

			LDR	R6, R1, #0			; Set R6 = R_SR_C <-M[R_SR] ; Load the content of the ROW address.
INNERLOOP	

			BRn	ISNEG				; Checks if previous R_SR_C register is negative ; Branches to ISNEG if it is negative.

			LDI	R0,	SPACE			; R0 <- M[Rff] ; Loads the symbol into R0 to be printed if bit is set to 0 on the font_data.		
			OUT						; PRINT OUT SYMBOL FOR "0"

			BR ZEROCHK				; Branches to ZEROCHK to skip ISNEG pseudo-op

ISNEG		LDI	R0,	CHAR			; R0 <- M[Rff + 1] ; Loads the symbol into R0 to be printed if bit is set to 1 on the font_data.	 			
			OUT						; PRINT OUT SYMBOL FOR "1"

ZEROCHK		ADD	R5, R5, #-1			; Decrements column counter

			BRz	ISZERO				; When Column counter == 0, Branch to ISZERO

			ADD	R6, R6, R6			; Left shift the ROW content by adding, R_SR_C <- R_SR_C + R_SR_C 

			BR	INNERLOOP			; Branch back up to INNERLOOP (since column counter != 0)

ISZERO		LD	R0, ENTER			; R0 <- x0A	; Loads the address for enter into R0 to create a new line. 
			OUT						; Print ENTER

			ADD R1, R1, #1			; Increment the ROW location by 1

			ADD R4, R4, #-1			; Decrement the ROW count, R_row_count <- R_row_count - 1

			BRp OUTERLOOP			; If R_row_count is still positive (16 rows aren't finished), Branch back up to OUTERLOOP

EXIT		HALT					; EXIT or DONE

SPACE		.FILL	x5000			; Memory Address for space, symbol for 0
CHAR 		.FILL	x5001			; Memory Address for character to print, symbol for 1
LETTR		.FILL	x5002			; Memory Address for letter
ENTER		.FILL	x0A				; Memory Address for enter

